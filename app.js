他一直保留着自己那颗最敏感的耳朵。他能在无数海浪噪音里分辨出一艘洛杉矶级的涡轮——1983年之后，再也没有任何人或任何东西值得他用那么细的耳朵去听。他的仪器睡了比他还久。所有核反应堆停了，所有声纳基阵从天花板上拆下，学生每一年问一样的问题。他一直听，但他听不见任何活物——直到那一年卡佳病危，他守监护仪前，忽然之间在那仪表尖啸里，他听见了年轻时北冰洋的水下：俄亥俄的低鸣、厚冰层融化在舱壁外面的嘎啦声、卡佳三十岁那天的笑声。

他没有告诉任何人。一个从被除名的潜艇上退役的声纳官，在妻子病床旁边听出了一生最强的混响。他关掉监护仪的低噪报警，握住她手指说：卡佳，你他妈的能不能先别走。她没有回答。那是他从1974年到她生命最后一刻收到的唯一一次无应答。

